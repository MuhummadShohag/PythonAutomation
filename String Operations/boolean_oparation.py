my_string="Python Script"

# it is ending with p, result showed True or False.
print(my_string.endswith("p"))

# it is starting with p, result showed True or False.
print(my_string.startswith("p"))

# It is lower
print(my_string.islower())

# It is upper
print(my_string.isupper())

# it title look like very each word beging with capitalize
print(my_string.istitle())

# it has space
print(my_string.isspace())

# it has consist of only alphabets,if has space with sentence it is showed False
print(my_string.isalpha())

# it is numeric
print(my_string.isnumeric())





